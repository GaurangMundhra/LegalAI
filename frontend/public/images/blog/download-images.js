// This is a Node.js script to download the images
// You can run it using: node download-images.js

const https = require('https');
const fs = require('fs');
const path = require('path');

images.forEach(image => {
  const file = fs.createWriteStream(path.join(__dirname, image.name));
  https.get(image.url, response => {
    response.pipe(file);
    file.on('finish', () => {
      file.close();
      console.log(`Downloaded ${image.name}`);
    });
  }).on('error', err => {
    fs.unlink(image.name);
    console.error(`Error downloading ${image.name}: ${err.message}`);
  });
}); 